"""Weekly report generation from incumbent predictions.

Reads the production prediction CSV and generates a readable weekly report
with game table, confidence summary, caution flags, and research disclaimer.
"""

import argparse
from pathlib import Path
from typing import List, Optional

import pandas as pd

from sportslab.evaluation.predict_incumbent import (
    INCUMBENT_CALIBRATION,
    INCUMBENT_DATE,
    INCUMBENT_FEATURE_SET,
    INCUMBENT_HOLDOUT_LL,
    INCUMBENT_REPORT,
    INCUMBENT_VAL_LL,
    INCUMBENT_VERSION,
)

PREDICTIONS_PATH = Path("reports/predictions/incumbent_predictions.csv")
DEFAULT_OUTPUT = Path("reports/predictions/weekly_report.md")

ALL_CAUTION_COLS = [
    "caution_qb_change",
    "caution_neutral",
    "caution_early_season",
    "caution_missing_features",
    "caution_model_market_disagreement",
]

CAUTION_LABELS = {
    "caution_qb_change": "QB change",
    "caution_neutral": "Neutral site",
    "caution_early_season": "Early season (W1-4)",
    "caution_missing_features": "Missing features",
    "caution_model_market_disagreement": "Model-market disagreement",
}


def _detect_last_season_week(df: pd.DataFrame) -> tuple:
    max_row = df.loc[df["season"].idxmax()]
    return int(max_row["season"]), int(max_row["week"])


def _cautions_for_row(row: pd.Series) -> List[str]:
    flags = []
    for col, label in CAUTION_LABELS.items():
        if col in row.index and row.get(col, 0) == 1:
            flags.append(label)
    return flags


def _write_weekly_report(
    df: pd.DataFrame,
    season: int,
    week: int,
    output: Path,
) -> None:
    games = df[(df["season"] == season) & (df["week"] == week)]
    if games.empty:
        with open(output, "w") as f:
            f.write(f"# Weekly Report — Week {week}, {season} Season\n\n")
            f.write(f"No games found for {season} week {week}.\n")
        return

    games = games.sort_values("gameday").reset_index(drop=True)

    with open(output, "w") as f:
        f.write(f"# Weekly Report — NFL Week {week}, {season} Season\n\n")
        f.write(
            f"*Generated by `sportslab weekly-report` ({INCUMBENT_VERSION}, {INCUMBENT_DATE})*\n\n"
        )

        # ── Model Metadata ──
        f.write("## Model Metadata\n\n")
        f.write("| Attribute | Value |\n")
        f.write("|-----------|-------|\n")
        f.write(f"| Model version | {INCUMBENT_VERSION} |\n")
        f.write(f"| Feature set | {INCUMBENT_FEATURE_SET} |\n")
        f.write(f"| Calibration | {INCUMBENT_CALIBRATION} |\n")
        f.write(f"| Validation LL | {INCUMBENT_VAL_LL} |\n")
        f.write(f"| Holdout LL (2025) | {INCUMBENT_HOLDOUT_LL} |\n")
        f.write(f"| Experiment report | {INCUMBENT_REPORT} |\n\n")

        # ── Games Table ──
        f.write("## Games\n\n")
        f.write(
            "| Date | Away | Home | Predicted Winner"
            " | Home Win Prob | Bucket | Cautions | Market vs Model |\n"
        )
        f.write(
            "|------|------|------|----------------"
            "|-------------|--------|----------|----------------|\n"
        )

        for _, row in games.iterrows():
            cautions = _cautions_for_row(row)
            caution_str = "; ".join(cautions) if cautions else "—"
            mkt_diff = row.get("market_model_diff", "")
            mkt_str = f"{mkt_diff:+.3f}" if pd.notna(mkt_diff) and mkt_diff != "" else "—"

            f.write(
                f"| {row['gameday']}"
                f" | {row['away_team']}"
                f" | {row['home_team']}"
                f" | {row['predicted_winner']}"
                f" | {row['incumbent_home_win_prob']:.3f}"
                f" | {row['confidence_bucket']}"
                f" | {caution_str}"
                f" | {mkt_str} |\n"
            )

        f.write("\n")

        # ── Highest-confidence games ──
        f.write("## Highest-Confidence Games\n\n")
        top_n = min(5, len(games))
        top_games = games.nlargest(top_n, "incumbent_home_win_prob")
        f.write("| Game | Prob | Predicted | Bucket | Cautions |\n")
        f.write("|------|------|-----------|--------|----------|\n")
        for _, row in top_games.iterrows():
            cautions = _cautions_for_row(row)
            caution_str = "; ".join(cautions) if cautions else "—"
            f.write(
                f"| {row['away_team']} @ {row['home_team']}"
                f" | {row['incumbent_home_win_prob']:.3f}"
                f" | {row['predicted_winner']}"
                f" | {row['confidence_bucket']}"
                f" | {caution_str} |\n"
            )
        f.write("\n")

        # ── Caution games ──
        caution_mask = games[[c for c in ALL_CAUTION_COLS if c in games.columns]].sum(axis=1) > 0
        caution_games = games[caution_mask]
        if not caution_games.empty:
            f.write("## Games with Cautions\n\n")
            f.write(
                "Games flagged for one or more caution conditions:"
                " QB change, neutral site, early season, or model-market disagreement.\n\n"
            )
            f.write("| Game | Prob | Predicted | Cautions |\n")
            f.write("|------|------|-----------|----------|\n")
            for _, row in caution_games.iterrows():
                cautions = _cautions_for_row(row)
                f.write(
                    f"| {row['away_team']} @ {row['home_team']}"
                    f" | {row['incumbent_home_win_prob']:.3f}"
                    f" | {row['predicted_winner']}"
                    f" | {'; '.join(cautions)} |\n"
                )
            f.write("\n")

        # ── Market-Aware Caveat ──
        f.write("## Caveats\n\n")
        f.write(
            "This is a **research output** produced by the SportsLab NFL"
            " prediction research project. It is intended for educational and"
            " research purposes only.\n\n"
        )
        f.write(
            "**This is not betting or investment advice.** The model's predictions"
            " are purely pregame, football-only, and do not incorporate market"
            " data, injury report nuances, or late-breaking information.\n\n"
        )
        f.write(
            "The closing moneyline market (no-vig) achieves 0.6090 holdout"
            " log loss vs the incumbent's 0.6262. Market data fields in this"
            " report are provided for **diagnostic comparison only** and are not"
            " used in model predictions.\n\n"
        )
        f.write(
            "**Do not use this output for gambling decisions.** The model has"
            " known failure modes (QB-change games, extreme confidence, early"
            " season, Monday night games) and is not calibrated for financial"
            " risk.\n\n"
        )
        f.write("---\n")
        f.write(
            f"*Report generated by SportsLab NFL Incumbent"
            f" {INCUMBENT_VERSION}. See {INCUMBENT_REPORT} for details.*\n"
        )


def generate_weekly_report(
    season: Optional[int] = None,
    week: Optional[int] = None,
    output: Optional[str] = None,
) -> str:
    if not PREDICTIONS_PATH.exists():
        raise FileNotFoundError(
            f"Prediction file not found: {PREDICTIONS_PATH}. Run `make predict-incumbent` first."
        )

    df = pd.read_csv(PREDICTIONS_PATH)

    if season is None or week is None:
        season, week = _detect_last_season_week(df)
        print(f"  Latest: {season} week {week}")

    if week < 1 or week > 22:
        raise ValueError(
            f"Week must be in [1, 22], got {week}. NFL regular season: weeks 1-18; playoffs: 19-22."
        )

    min_season, max_season = int(df["season"].min()), int(df["season"].max())
    if season < min_season or season > max_season:
        raise ValueError(f"Season must be in [{min_season}, {max_season}], got {season}.")

    output_path = Path(output) if output else DEFAULT_OUTPUT
    output_path.parent.mkdir(parents=True, exist_ok=True)

    _write_weekly_report(df, season, week, output_path)

    n_games = len(df[(df["season"] == season) & (df["week"] == week)])
    print(f"Weekly report: {output_path} ({n_games} games)")
    return str(output_path)


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate weekly report from incumbent predictions"
    )
    parser.add_argument("--season", type=int, default=None, help="Season year")
    parser.add_argument("--week", type=int, default=None, help="Week number")
    parser.add_argument(
        "--output",
        type=str,
        default=None,
        help="Output path (default: reports/predictions/weekly_report.md)",
    )
    args = parser.parse_args()
    generate_weekly_report(season=args.season, week=args.week, output=args.output)


if __name__ == "__main__":
    main()
